package JAVA_PROGRAMMING.TASK_JAVA;
//PATTERN- HALF PYRAMID WITH NUMBERS
public class AE02_TASK11 {
    public static void main(String[] args) {
        for(int i=1;i<5;i++)
        {
            for(int j=1;j<=i;j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
