package JAVA_PROGRAMMING.TASK_JAVA;
//REVRSE HALF PYRAMID
public class AD_TASK9 {
    public static void main(String[] args) {
        //OUTER LOOP
        for(int i=4;i>=1;i--)
        {
            //INNER LOOP
            for(int j=1;j<=i;j++)
            {
                System.out.print("*");
            }
            System.out.println();
        }

        
    }
}
