package JAVA_PROGRAMMING.CORE_JAVA;
//Printing 0-10 using While loop
public class A11_While_loop {
    public static void main(String[] args) {
        int i=0; //Initialisation
        while(i<=10) //while(Condition)
        {
            System.out.println(i); //Print statement
            i++; //Increement / Decreement
        }
    }
}
