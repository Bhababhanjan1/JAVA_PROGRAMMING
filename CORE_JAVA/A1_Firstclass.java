package JAVA_PROGRAMMING.CORE_JAVA;

public class A1_Firstclass {
    public static void main(String[] args) {    //main is a Function
        System.out.println("Hello, World!");//println-->Prints in a new line 
        System.out.print("Hello World");//print-->Prints in the same line
        System.out.print("Hello World \n");// \n-->Prints in a new Line(Similar to println)
        System.out.print("Hello World");
    }
    
}
