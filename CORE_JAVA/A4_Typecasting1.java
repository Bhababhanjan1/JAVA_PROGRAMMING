package JAVA_PROGRAMMING.CORE_JAVA;
//Implicit type casting
public class A4_Typecasting1 {
    public static void main(String[] args) {
        int a = 10;
        float f=a;
        short s=345;
        double d=s;
        System.out.println(f);
        System.out.println(d); //Coversion of smaller data type into bigger data type
    }
}
